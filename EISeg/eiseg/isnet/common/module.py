class UpBlock(nn.Module):
    """Upscaling then conv"""

    def __init__(self, in_channels, out_channels, nb_Conv, nb_upsample, activation='ReLU'):
        super(UpBlock, self).__init__()
        up_lst = []
        up_lst.append(nn.ConvTranspose2d(in_channels, out_channels,(2,2),2))
        for _ in range(nb_upsample):
            up_lst.append(nn.ConvTranspose2d(out_channels,out_channels,(2,2),2))
        self.n_upsample = nn.Sequential(*up_lst)
        
        self.nConvs = _make_nConv(out_channels * 2, out_channels, nb_Conv)

    def forward(self, x, skip_x):
        out = self.n_upsample(x)
        x = torch.cat([out, skip_x], dim=1)  # dim 1 is the channel dimension
        return self.nConvs(x)
